                                글로벌 이너웨어 전문기업 엠코르셋㈜가 전개하는 '
원더브라(Wonderbra)'가 2016년 첫 신제품 '미란다커 콜라보레이션 풀커버리지 패키지'를 1월 10일 GS홈쇼핑에서 공개했다. 

미란다커 콜라보레이션 풀커버리지 패키지는 원더브라 신제품 라인업 중 
더욱 섹시한 제품을 엄선해 구성한 특별 패키지다. 세련미가 돋보이는 블랙 세트, 
고급스러운 와인색 그라데이션이 들어가 더욱 섹시한 와인 세트, 쉬크한 그레이 컬러에 호피무늬로 여성미를 더한 그레이 세트, 은은한 광택이 우아함을 더한 베이지 세트 등 총 4가지 컬러의 세련되고 섹시한 디자인이 돋보이는 패키지다. 

2009년 한국 론칭 이후 지속적인 성장세를 이어가고 있는 원더브라는 2014년 미란다커를 모델로 전격 발탁했으며 홈쇼핑에서 분당 2000만원, 주요 매장에서 한 달 1억원 이상의 매출을 기록했다. 

또 2015년에는 매출 700억 돌파로 불경기에 고전하고 있는 국내 언더웨어 업계의 이목을 집중시키고 있다.  

이번에 공개되는 미란다커 콜라보레이션 풀커버리지 패키지의 브라 또한 기존 원더브라 제품과 같이 저중심 설계에 몰드컵 내부의 오일몰드가 가슴을 모아주고 끌어올려 줘 풍부한 볼륨감을 더하는 기능을 기본으로 한다. 이번 특별 패키지는 가슴 전체를 탄력적으로 지지해주는 풀커버리지 몰드컵 사용으로 착용시 안정감을 주는 것은 물론, 모든 제품에 봉제선이 보이지 않도록 처리돼 매끄러운 실루엣 연출이 가능한 것이 특징이다. 

